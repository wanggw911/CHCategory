- (NSData *)ch_base64EncodedData {
    return [self base64EncodedDataWithOptions:0];
}

- (NSString *)ch_base64EncodedString {
    return [self base64EncodedStringWithOptions:0];
}

- (NSData *)ch_base64DecodedData {
    return [[NSData alloc] initWithBase64EncodedData:self options:0];
}

- (NSString *)ch_base64DataDecodedString {
    NSData *stringFromBase64Data = [[NSData alloc] initWithBase64EncodedData:self options:0];
    return [[NSString alloc] initWithData:stringFromBase64Data encoding:NSUTF8StringEncoding];
}

- (NSData *)ch_aes256EncryptDataWithKey:(NSString *)key {
    char keyPtr[kCCKeySizeAES256+1];
    bzero(keyPtr, sizeof(keyPtr));
    [key getCString:keyPtr maxLength:sizeof(keyPtr) encoding:NSUTF8StringEncoding];
    NSUInteger dataLength = [self length];
    size_t bufferSize = dataLength + kCCBlockSizeAES128;
    void *buffer = malloc(bufferSize);
    size_t numBytesEncrypted = 0;
    CCCryptorStatus cryptStatus = CCCrypt(kCCEncrypt, kCCAlgorithmAES128,
                                          kCCOptionPKCS7Padding | kCCOptionECBMode,
                                          keyPtr, kCCBlockSizeAES128,
                                          NULL,
                                          [self bytes], dataLength,
                                          buffer, bufferSize,
                                          &numBytesEncrypted);
    if (cryptStatus == kCCSuccess) {
        return [NSData dataWithBytesNoCopy:buffer length:numBytesEncrypted];
    }
    free(buffer);
    return nil;
}

